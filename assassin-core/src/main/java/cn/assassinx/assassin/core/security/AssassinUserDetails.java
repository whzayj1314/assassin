package cn.assassinx.assassin.core.security;

import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.userdetails.User;
import org.springframework.security.core.userdetails.UserDetails;

import java.util.Collection;
import java.util.Map;

/**
 * @author Barton
 */
public class AssassinUserDetails implements UserDetails {

	private User user;

	private Map userExtend;

	private String clientId;

	private boolean isAccountNonLocked;

	public AssassinUserDetails(User user, Map userExtend, String clientId, boolean isAccountNonLocked) {
		this.user = user;
		this.userExtend = userExtend;
		this.clientId = clientId;
		this.isAccountNonLocked = isAccountNonLocked;
	}

	public Map getUserExtend() {
		return userExtend;
	}

	public String getClientId() {
		return clientId;
	}

	@Override
	public Collection<? extends GrantedAuthority> getAuthorities() {
		return user.getAuthorities();
	}

	@Override
	public String getPassword() {
		return user.getPassword();
	}

	@Override
	public String getUsername() {
		return user.getUsername();
	}

	@Override
	public boolean isAccountNonExpired() {
		return true;
	}

	@Override
	public boolean isAccountNonLocked() {
		return isAccountNonLocked;
	}

	@Override
	public boolean isCredentialsNonExpired() {
		return true;
	}

	@Override
	public boolean isEnabled() {
		return true;
	}

}
